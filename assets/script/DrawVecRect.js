cc.Class({
    extends: cc.Component,

    properties: {
        // foo: {
        //     // ATTRIBUTES:
        //     default: null,        // The default value will be used only when the component attaching
        //                           // to a node for the first time
        //     type: cc.SpriteFrame, // optional, default is typeof default
        //     serializable: true,   // optional, default is true
        // },
        // bar: {
        //     get () {
        //         return this._bar;
        //     },
        //     set (value) {
        //         this._bar = value;
        //     }
        // },
    },

    // LIFE-CYCLE CALLBACKS:

    // onLoad () {},
    onLoad: function () {
        this.ctx = this.node.getComponent(cc.Graphics);
    },

    start () {
        //console.log(this.ctx);
    },

    // update (dt) {},
    update: function (dt) {
        this.ctx.clear();
        this.ctx.rect(this.node.x - this.node.width / 2 + this.node.parent.width / 2, this.node.y - this.node.height / 2 + this.node.parent.height / 2, this.node.width, this.node.height);
        this.ctx.fill();
    }
});
